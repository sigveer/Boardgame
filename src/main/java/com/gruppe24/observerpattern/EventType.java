package com.gruppe24.observerpattern;

/**
 * Enum representing different types of events in the game.
 * This enum is used to categorize events that can occur during the game.
 */
public enum EventType {
  DICE_ROLLED,
  PLAYER_MOVED,
  PLAYER_ADDED,
  PLAYER_REMOVED,
  PLAYER_ICON_CHANGED,
  GAME_STARTED,
  GAME_ENDED
}
